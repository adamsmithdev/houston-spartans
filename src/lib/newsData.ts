/**
 * Get all news articles sorted by date (newest first)
 * @returns {ReadonlyArray<NewsArticle>} All available news articles
 */
export const getAllArticles = (): ReadonlyArray<NewsArticle> => {
	return newsArticlesData;
};

/**
 * Get a specific article by its slug identifier
 * @param {string} slug - The unique slug identifier for the article
 * @returns {NewsArticle | null} The article if found, null otherwise
 */
export const getArticleBySlug = (slug: string): NewsArticle | null => {
	return newsArticlesData.find((article) => article.slug === slug) || null;
};

/**
 * Get related articles excluding the current article
 * @param {string} currentSlug - Slug of the current article to exclude
 * @param {number} [limit=3] - Maximum number of related articles to return
 * @returns {ReadonlyArray<NewsArticle>} Array of related articles
 */
export const getRelatedArticles = (
	currentSlug: string,
	limit: number = 3,
): ReadonlyArray<NewsArticle> => {
	return newsArticlesData
		.filter((article) => article.slug !== currentSlug)
		.slice(0, limit);
};

/**
 * Get articles filtered by category
 * @param {string} category - The category to filter by
 * @returns {ReadonlyArray<NewsArticle>} Articles matching the specified category
 */
export const getArticlesByCategory = (
	category: string,
): ReadonlyArray<NewsArticle> => {
	return newsArticlesData.filter((article) => article.category === category);
};

/**
 * Get all article slugs for static site generation
 * @returns {ReadonlyArray<string>} Array of all article slugs
 */
export const getAllSlugs = (): ReadonlyArray<string> => {
	return newsArticlesData.map((article) => article.slug || article.id);
};
